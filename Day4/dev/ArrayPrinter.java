package com.cdac.acts.dev;

public class ArrayPrinter {
	
	
	
		public ArrayPrinter(){}
	
	
		
		public static void printArray(int[] arr) {
			
			for(int i = 0; i < arr.length ; i ++)
			{
				System.out.println(arr[i]);
				
			}
		}
		public static void printArray(String[] arr) {
			
			for(int i = 0; i < arr.length ; i ++)
			{
				System.out.println(arr[i]);
				
			}
		}

}
